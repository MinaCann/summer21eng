package day03increment_decrement_operators;

public class OrderOfOparation01 {

	public static void main(String[] args) {

		//38/2-(4+3)*2=?
		System.out.println(38/2-(4+3)*2);//5
	
		//8+2*(14-6/2)-12=?
		System.out.println(8+2*(14-6/2)-12);//18
		
	/* order of operations
		1_ ()
		2_ Exponents
		3_ Multiplication and division
		4_ Addition and subtractions
		
		
	*/	
	}

}
