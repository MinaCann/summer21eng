package day03increment_decrement_operators;

public class Concatenation01 {

	public static void main(String[] args) {
		
		/*
	 	Concatenation: To join Strings
	*/

		
		String fn = "Ali";
		String ln = "Can";
		
		System.out.println(fn + ln); //AliCan
		
		System.out.println(fn + " " + ln); //Ali Can
		
		
		String fn2 = "Ayse";
		String ln2 = " Han";
		
		System.out.println(fn2 + ln2); //Ayse Han
		
		//Question
		System.out.println(fn + 2 + 3); //Ali23
		System.out.println(fn + (2 + 3)); //Ali5
		System.out.println(2 + 3 + fn); //5Ali
		
		System.out.println(2 + fn + 3 * 2);//2Ali6
		
		
		
		
		int numA = 2;
		int numB = 3;
		
	}

}
